# concatenação de strings com operador de soma [+]
conactenacao = 'A' + 'B' + 'C'
print(conactenacao)

conactenacao = 'Antonio' + ' ' + 'Henrique'
print(conactenacao)

# repeticao de strings com operador de multiplicação [*]
a_dez_vezes = 'A' * 10
tres_vezes_antonio = 3 * 'Antonio'
print(a_dez_vezes, end='\n\n\n')
print(tres_vezes_antonio)
