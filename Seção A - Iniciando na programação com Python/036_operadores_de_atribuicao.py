"""
Operadores de atribuição
representam formas contraidas de operar e atribuir:

suponha que a seja 10 e b seja 5

= -> a = b (dessa forma a vale 5)

+= -> a += b (é o mesmo que a = a + b, ou seja, a vale 15)

-= -> a -= b (a = a - b, ou seja, a é 5)

*= -> a *= b ou a = a * b

/=

//=

**=

%=
"""

contador = 10
contador /= 5
print(contador)

a = 10
b = 5

a += b

print(a)
